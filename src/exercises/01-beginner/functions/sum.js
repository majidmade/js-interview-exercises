export const sumOfUnique = (values) => {
  // FIXME: The code below should return the sum of all unique numbers
  //        Only process if values is an array.
  //        Return 0 if values is invalid.
  const sum = 0;

  for(let i = 0; i < values.length; i++) {
    sum = sum + values[i];
  }

  return sum;
};